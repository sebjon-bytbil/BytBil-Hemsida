function getSiteSettings()
{
    $settings = null;
    $id = get_option("selected-settings-page");
    if (!!$id) {
        $settings = get_post($id);

        if (!$settings || $settings->post_status != "publish") {
            update_option("selected-settings-page", false);
            getSiteSettings();
        }
    } else {
        $posts = get_posts(array(
            'post_type' => 'sitesettings',
            'posts_per_page' => 1,
            'post_status' => 'publish'
        ));

        if (!empty($posts[0])) {
            $settings = $posts[0];
        }
    }

    return $settings;
}

function renderSettings($id = null)
{
    $settings = getSiteSettings($id);

    if ($settings) {
        $sid = $settings->ID;
        include __DIR__ . "/bbcms-styles.php";
    }
}

