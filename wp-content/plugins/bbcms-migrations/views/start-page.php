<h1>BytBil Migration Handler</h1>
<p>Detta är migreringshanteraren för BytBil CMS. Den kan användas för att uppdatera Advanced Custom Fields för alla
    siter i ett nätverk.</p>
<p>Är fältet av typen <code>checkbox</code> så kommer värdet att läggas till som "aktivt".</p>

<a href="admin.php?page=update-values">
    <h2>
        <li>Update Field Values</li>
    </h2>
</a>
<p>Ändra värdet i ett fält och applicera det på alla sajter i ett nätverk</p>
<a href="admin.php?page=change-field">
    <h2>
        <li>Change Field Names</li>
    </h2>
</a>
<p>Ändra fältnamn, värdet hämtas från ett fält och läggs in i ett nytt/annat fält.</p>
